package entity;

public class Education {
	
	private String degre;
	private String  formation;
	private String  school;
	private String  intendToStudyAgain;
	private String  pretendeStudy;
	public Education(String degre, String formation, String school, String intendToStudyAgain, String pretendeStudy) {
		super();
		this.degre = degre;
		this.formation = formation;
		this.school = school;
		this.intendToStudyAgain = intendToStudyAgain;
		this.pretendeStudy = pretendeStudy;
	}
    
}
